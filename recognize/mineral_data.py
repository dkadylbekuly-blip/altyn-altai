"""
MINERAL_INFO — справочник карточек минералов/пород для проекта распознавания.
Ключи словаря должны совпадать с нормализованными label модели (lowercase, без пробелов и цифр на конце).

Поля, которые уже используются в шаблонах:
- title, formula, color, hardness, origin, usage, description

Доп. поля для «вау-уровня»:
- type: mineral | rock | mineraloid
- facts: список интересных фактов
- hazards: предупреждения по безопасности
- sources: подсказка, откуда брать/проверять (короткие ссылки-идентификаторы, можно расширять)

Если конкретного класса ещё нет в MINERAL_INFO, get_mineral_info() вернёт аккуратный плейсхолдер,
и сайт не будет выглядеть «пустым».
"""

from __future__ import annotations
import re
from typing import Dict, Any, Optional


# --- Алиасы, чтобы разные написания вели к одной карточке ---
ALIASES = {
    # варианты названий
    "baryte": "barite",
    "fluorspar": "fluorite",
    "rocksalt": "halite",
    "native_copper": "nativecopper",
    "smoky_quartz": "smokyquartz",
    "rose_quartz": "rosequartz",
    "lapis_lazuli": "lapislazuli",
    "nativecopper1": "nativecopper",
    # на будущее можно добавлять свои
}


def normalize_label(label: str) -> str:
    """
    Приводит метку модели к ключу MINERAL_INFO:
    - lower
    - убирает пробелы/дефисы/подчёркивания
    - убирает цифры в конце (agate2 -> agate)
    """
    if not label:
        return ""
    s = label.strip().lower()
    s = s.replace(" ", "").replace("-", "").replace("_", "")
    s = re.sub(r"\d+$", "", s)  # удалить цифры в конце
    return s


def make_placeholder(raw_label: str) -> Dict[str, Any]:
    """
    Карточка-заглушка, чтобы интерфейс всегда показывал информацию,
    даже если минерал ещё не внесён.
    """
    key = normalize_label(raw_label)
    return {
        "title": raw_label,
        "type": "unknown",
        "formula": "—",
        "color": "—",
        "hardness": "—",
        "origin": "Информация уточняется (минерал/порода ещё не добавлены в справочник).",
        "usage": "—",
        "description": (
            "Для этого класса пока нет подробной музейной карточки. "
            "Добавьте запись в MINERAL_INFO, чтобы показывать формулу, свойства и интересные факты."
        ),
        "facts": [],
        "hazards": [],
        "sources": {},
        "key": key,
    }


def get_mineral_info(model_label: str) -> Dict[str, Any]:
    """
    Основная функция доступа: нормализует label + применяет алиасы + отдаёт карточку или плейсхолдер.
    """
    key = normalize_label(model_label)
    key = ALIASES.get(key, key)
    return MINERAL_INFO.get(key) or make_placeholder(model_label)


